char	*ft_strupcase(char *str)
{
	char *pbeg;

	pbeg = str;
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
			*str = *str + ('A' - 'a');
		++str;
	}
	return (pbeg);
}
